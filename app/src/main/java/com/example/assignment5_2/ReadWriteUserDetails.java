package com.example.assignment5_2;

public class ReadWriteUserDetails {

public String userName, userPhone, userMail, userCountry, userAddress, userGender, userDob, userHobey, userStd1, userStd2, userDegree, userPerStd1, userPerStd2, userPerDegree, userPassword;

public ReadWriteUserDetails(){};
    public ReadWriteUserDetails(String userName, String userPhone, String userMail, String userCountry, String userAddress, String userGender, String userDob, String userHobey, String userStd1, String userStd2, String userDegree, String userPerStd1, String userPerStd2, String userPerDegree, String userPassword) {
        this.userName = userName;
        this.userPhone = userPhone;
        this.userMail = userMail;
        this.userCountry = userCountry;
        this.userAddress = userAddress;
        this.userGender = userGender;
        this.userDob = userDob;
        this.userHobey = userHobey;
        this.userStd1 = userStd1;
        this.userStd2 = userStd2;
        this.userDegree = userDegree;
        this.userPerStd1 = userPerStd1;
        this.userPerStd2 = userPerStd2;
        this.userPerDegree = userPerDegree;
        this.userPassword = userPassword;
    }
}
